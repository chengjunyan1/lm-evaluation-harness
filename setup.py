def gen_blimp_filtered(task_dir,data_dir: str) -> None:
    for task in blimp_filtered_subtasks:
        file_name = f"{task}.yaml"
        try:
            with open(f"{task_dir}/{file_name}", "w", encoding="utf-8") as f:
                f.write("# Generated by utils.py\n")
                yaml.dump(
                    {
                        "include": "_template_yaml",
                        "task": "blimp_" + task + "_filtered",
                        "dataset_kwargs": {"data_files": os.path.join(data_dir, "blimp_filtered", task + ".jsonl")}
                    },
                    f,
                )
        except FileExistsError:
            pass


